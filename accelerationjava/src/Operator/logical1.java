package Operator;

public class logical1 {
	
	public static void main(String[] args) {
		
		boolean bool1=false,  bool2 = true;
		
		System.out.println("bool1 && bool2 :   "+(bool1 && bool2));
		
		System.out.println("bool1 || bool2 :  "+(bool1 || bool2));
		
		System.out.println("!(bool1 && bool2):   "+!(bool1 && bool2));
		
	}

}
