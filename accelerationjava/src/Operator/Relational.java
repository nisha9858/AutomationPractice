package Operator;

public class Relational {
	
	public static void main(String[] args) {
		
		int num1=-5, num2=-4;
		
		System.out.println("num1: "+ num1+"----->num2"  +num2);
		
		boolean res=(num1==num2);
		
		System.out.println("boolean res  "+res);
		
		System.out.println("num1>num2  "+(num1>num2));
		
		System.out.println("num1>=num2  "+(num1>=num2));
		
		System.out.println("num1<num2  "+(num1<num2));
		
		System.out.println("num1<=num2  "+(num1<=num2));
		
		System.out.println("num1!=num2  "+(num1!=num2));
		
	}

}
