package SwitchCase;

public class Switch2 {
	
	public static void main(String[] args) {
		
		int month =3;
		
		switch(month) {
		
		case 1:
			System.out.println("Congratulations month is January");
			break;
			
		case 2:
			System.out.println("Congratulations month is February");
			break;
			
		case 3:
			System.out.println("Congratulations month is March");
			break;
			
		case 4:
			System.out.println("Congratulations month is April");
			break;
			
		default:
			System.out.println("Sorry");
			break;
		}
		
	}

}
