package ifelse;

public class forloop4 
{ 
	public static void main(String[] args) {
		
		for(int i=1;i<6;i++)
		{
			System.out.println("Hello Goodmorning");
			}
		
		for(int i=1; i<=5;i++)
		{  
			System.out.println(i);         
			}
		
		for(int i=10; i>0;i--) 
		{
			System.out.println(i);
		}
		
	}

}
