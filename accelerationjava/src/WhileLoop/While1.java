package  WhileLoop;

public class While1 {
	
	public static void main(String[] args) {
		
		int i=0;
		
		System.out.println("Printing number from 1 to 5");
		
		while(i<5) {
			System.out.println("Hello   "+i); // hello  0 to 4
			
			i++;
		}
		
		int a=5;
		
		while(a>0) { 
			
			System.out.println("bye   "+a); // bye  5 to 1
			
			a--;
		}
		
	}

}
